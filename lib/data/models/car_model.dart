class Car {
  final String carName;
  final String carClass;
  final double carPower;
  final String people;
  final String bags;
  final bool bluetooth;
  final String carImage;
  final double carPrice;
  final bool isRotated;
  final double carRating;
  final int storeId;

  Car({required this.carName, required this.carClass, required this.carPower, required this.people, required this.bags, required this.bluetooth, required this.carImage, required this.carPrice, required this.isRotated, required this.carRating, required this.storeId});
}